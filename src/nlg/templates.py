def get_absolute_clock(image_clock: str, camera_orientation: str) -> str:
    """
    이미지 기준 시계 방향을 실제(절대) 시계 방향으로 보정.

    예시:
      폰을 오른쪽으로 들고(camera_orientation="right") 촬영했을 때
      이미지 중앙(12시)에 의자가 있으면 실제로는 오른쪽(3시)에 있음
      → get_absolute_clock("12시", "right") = "3시"

    Args:
      image_clock: ZONE_BOUNDARIES에서 결정된 이미지 기준 방향
      camera_orientation: 가속도 센서가 감지한 폰 방향 (front/back/left/right)

    Returns:
      실제 방향 기준 시계 방향 문자열
    """
    offset = CAMERA_OFFSET.get(camera_orientation, 0)
    # CLOCK_HOURS 배열에서 해당 방향의 인덱스 찾기
    idx = CLOCK_HOURS.index(image_clock) if image_clock in CLOCK_HOURS else 0
    # 오프셋 더하고 12시간 순환 (% 12)
    return CLOCK_HOURS[(idx + offset) % 12]
